std::string utf8_to_big5(const std::string& utf8) {
    if (utf8.empty()) return "";

    // Step 1: UTF-8 → UTF-16
    int wlen = MultiByteToWideChar(CP_UTF8, 0,
                                   utf8.c_str(), (int)utf8.size(),
                                   nullptr, 0);
    std::wstring wstr(wlen, 0);
    MultiByteToWideChar(CP_UTF8, 0,
                        utf8.c_str(), (int)utf8.size(),
                        &wstr[0], wlen);

    // Step 2: UTF-16 → Big5 (CP950)
    int len = WideCharToMultiByte(950, 0,
                                  wstr.c_str(), (int)wstr.size(),
                                  nullptr, 0, nullptr, nullptr);
    std::string big5(len, 0);
    WideCharToMultiByte(950, 0,
                        wstr.c_str(), (int)wstr.size(),
                        &big5[0], len,
                        nullptr, nullptr);

    return big5;
}

BattleCatData::BattleCatData()
{
    auto start = std::chrono::high_resolution_clock::now();

    idToUnitName = unordered_map<int32_t, string>();
    unitNameToId = unordered_map<string, int32_t>();
    loadUnits();
    loadBanners();

    auto end = std::chrono::high_resolution_clock::now();
    std::chrono::duration<double> elapsed = end - start;
    std::cout << "BattleCatData initialization time: "
              << std::fixed << std::setprecision(4)
              << elapsed.count() << " seconds\n";
}

