unordered_map<int, string> getIdToName()
{
    std::ifstream in("bc-tw.yaml");
    //in.imbue(std::locale(std::locale(), new std::codecvt_utf8<char>));

    unordered_map<int, string> idToName;
    string line;
    int currentId = -1;

    std::regex idRegex(R"(^\s*(\d+):\s*$)");
    std::regex nameRegex(R"(^\s*name:\s\[(.*)\])");

    while (std::getline(in, line)) {
        std::smatch match;
        if (std::regex_match(line, match, idRegex)) {
            currentId = std::stoi(match[1]);
        } else if (std::regex_match(line, match, nameRegex) && currentId != -1) {
            string names = match[1];
            size_t pos = names.find(',');
            string firstName = (pos == string::npos) ? names : names.substr(0, pos);
            // trim spaces
            firstName.erase(0, firstName.find_first_not_of(" \t"));
            firstName.erase(firstName.find_last_not_of(" \t") + 1);
            // idToName[currentId] = firstName;
            idToName[currentId] = utf8_to_big5(firstName);
        }
    }
    return idToName;
}

